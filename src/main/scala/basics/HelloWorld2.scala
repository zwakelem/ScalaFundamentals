@main
// multi-line function
def main(): Unit = {
  println("Hello \"World\"") // escaped double quote
  println("Hello Zwaks")
  println("Hello \\Scala\\") // escaped back slash
  println("Hello \tScala") // Tab
  println("Hello \nScala") // new line
  println("Hello \bScala") // backspace, deletes 1 character
}
